    /** Maps IMAGE_RESOLUTION protocol enum values to (width, height) pixel dimensions. */
    fun resolutionSize(resolution: Int): Pair<Int, Int> = when (resolution) {
        1 -> Pair(8,   8)     // QQQQVGA
        2 -> Pair(80,  60)    // QQQVGA
        3 -> Pair(160, 120)   // QQVGA
        4 -> Pair(320, 240)   // QVGA
        5 -> Pair(512, 384)   // CVGA
        6 -> Pair(640, 480)   // VGA
        else -> Pair(160, 120) // default to QQVGA
    }

