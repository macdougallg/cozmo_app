import java.io.ByteArrayOutputStream

/**
 * Decodes Cozmo's "minimized JPEG" camera frame formats into standard decodable JPEG.
 *
 * The robot sends raw JPEG entropy-coded data without the standard JPEG frame headers
 * (JFIF, quantization tables, Huffman tables, SOF, SOS) to save bandwidth. This decoder
 * prepends the fixed headers, applies byte-stuffing (0xFF → 0xFF 0x00), and appends
 * the EOI marker to produce a valid JPEG that Android's BitmapFactory can decode.
