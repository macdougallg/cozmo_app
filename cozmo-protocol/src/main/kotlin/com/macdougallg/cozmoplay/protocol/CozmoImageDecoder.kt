    // ── Public API ────────────────────────────────────────────────────────────

    /**
     * Converts JPEGMinimizedGray (enc=8) entropy data into a decodable JPEG.
     *
     * @param data   Raw entropy-coded bytes from the assembled ImageChunk frame.
     * @param width  Frame width in pixels (from IMAGE_RESOLUTION enum).
     * @param height Frame height in pixels (from IMAGE_RESOLUTION enum).
     * @return Valid JPEG bytes suitable for BitmapFactory.decodeByteArray().
     */
    fun miniGrayToJpeg(data: ByteArray, width: Int, height: Int): ByteArray =
        miniToJpegHelper(data, width, height, MINIGRAY_HEADER)

    /**
     * Converts JPEGMinimizedColor (enc=9) entropy data into a decodable JPEG.
     * The robot encodes color as YCbCr with 2x1 chroma subsampling.
     */
    fun miniColorToJpeg(data: ByteArray, width: Int, height: Int): ByteArray =
        miniToJpegHelper(data, width, height, MINICOLOR_HEADER)

    // ── Shared helper ─────────────────────────────────────────────────────────

    private fun miniToJpegHelper(data: ByteArray, width: Int, height: Int, headerTemplate: ByteArray): ByteArray {
        val header = headerTemplate.copyOf()
        header[0x5e] = (height ushr 8).toByte()
        header[0x5f] = (height and 0xFF).toByte()
        header[0x60] = (width ushr 8).toByte()
        header[0x61] = (width and 0xFF).toByte()

        var dataLen = data.size
        while (dataLen > 0 && data[dataLen - 1] == 0xFF.toByte()) dataLen--

        val out = ByteArrayOutputStream(header.size + dataLen + dataLen / 4 + 2)
        out.write(header)

        // Byte-stuffing: skip first byte, insert 0x00 after every 0xFF
        for (i in 0 until dataLen - 1) {
            val b = data[i + 1].toInt() and 0xFF
            out.write(b)
            if (b == 0xFF) out.write(0x00)
        }

        out.write(0xFF)
        out.write(0xD9)
        return out.toByteArray()
    }
